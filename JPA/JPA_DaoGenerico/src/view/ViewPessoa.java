    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        // TODO add your handling code here:
        Object [] conts = pdao.obtemTodos(Pessoa.class).toArray();
        long id = 1;
        if (conts.length != 0) {
            Pessoa ultimo = (Pessoa) conts[conts.length-1];
            id = ultimo.getId() + 1;
        }
        limparCampos();
        txtId.setText(String.valueOf(id));
        txtNome.requestFocusInWindow();
        btnNovo.setEnabled(false);
        btnSalvar.setEnabled(true);
        btnEditar.setEnabled(false);
        btnRemover.setEnabled(false);
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        long id = Integer.parseInt(txtId.getText());
        String nome = txtNome.getText();
        //String telefone = txtTelefone.getText();
        Pessoa p = new Pessoa();
        p.setId(id);
        p.setNome(nome);
        ArrayList<Telefone> tels = new ArrayList<>();
        for (int i=0; i<this.modeloListaTelefones.size(); ++i) {
            tels.add(new Telefone(this.modeloListaTelefones.get(i)));
        }
        p.setTelefones(tels);
        pdao.grava(Pessoa.class, p);
        atualizaTabelaPessoas();
        limparCampos();
        btnSalvar.setEnabled(false);
        btnNovo.setEnabled(true);
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if(table.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um contato na lista para editar");
        }	
        else {
            atualizaCampos();
            txtNome.requestFocusInWindow();
            btnNovo.setEnabled(true);
            btnRemover.setEnabled(false);
            btnEditar.setEnabled(false);
            btnSalvar.setEnabled(true);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        if(table.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um contato na lista para remover");
        }	
        else {
            long id = (Long) table.getModel().getValueAt(table.getSelectedRow(),0);
            pdao.apaga(Pessoa.class, pdao.obtemPorID(Pessoa.class, id));
            atualizaTabelaPessoas();
            limparCampos();
            btnRemover.setEnabled(false);
            btnEditar.setEnabled(false);
            btnNovo.setEnabled(true);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        atualizaTabelaPessoas();
        btnSalvar.setEnabled(false);
        if (this.table.getModel().getRowCount() != 0 ) {
            this.table.setRowSelectionInterval(0, 0);
            atualizaCampos();
        }
        else {
            btnEditar.setEnabled(false);
            btnRemover.setEnabled(false);
        }
    }//GEN-LAST:event_formComponentShown

    private void btnRemoverTelefoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverTelefoneActionPerformed
        // TODO add your handling code here:
        int selec = listaTel.getSelectedIndex();
        if (selec != -1)
            this.modeloListaTelefones.remove(selec);
    }//GEN-LAST:event_btnRemoverTelefoneActionPerformed

