    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int r = this.table.getSelectedRow();
        if ( r != -1 ) {
            atualizaCampos();
            btnEditar.setEnabled(true);
            btnRemover.setEnabled(true);
            btnSalvar.setEnabled(false);
        }
    }//GEN-LAST:event_tableMouseClicked

    
    private void atualizaTabelaPessoas() {
        List<Pessoa> pessoas = pdao.obtemTodos(Pessoa.class);
        table.setModel(new PessoaTableModel(pessoas));		
    }   
    
    private void atualizaCampos() {
        int linha = table.getSelectedRow();
        Pessoa con = (Pessoa) pdao.obtemTodos(Pessoa.class).toArray()[linha];
        txtId.setText(Long.toString(con.getId()));
        txtNome.setText(con.getNome());
        atualizaTelefones(con.getTelefones());
    }
    
    private void limparCampos() {
        txtId.setText("");
        txtNome.setText("");
        this.modeloListaTelefones.clear();        
    }
    
    private void atualizaTelefones(Collection<Telefone> tels) {
        this.modeloListaTelefones.clear();
        for (Telefone t:tels) {
            this.modeloListaTelefones.addElement(t.getTelefone());
        }        
    }
